function pergunta1() {
    let botaoplus = document.getElementById("pergunta1plus")
    let botaominus = document.getElementById("pergunta1minus")
    if(botaoplus.hidden == false){
        document.getElementById("pergunta1").hidden = false;
        botaominus.hidden = false;
        botaoplus.hidden = true;
    }else if(botaominus.hidden == false){
        document.getElementById("pergunta1").hidden = true;
        botaominus.hidden = true;
        botaoplus.hidden = false;
    }
  }

